    print("After IQR outlier removal:", clean_df.shape)
    
    normalized_df = cleaner.normalize_minmax()
    print("After min-max normalization:")
    print(normalized_df[['feature1', 'feature2']].describe())
    
    filled_df = cleaner.fill_missing_mean()
    print("After filling missing values:", filled_df.isnull().sum().sum())

