    print("\nData after IQR outlier removal:")
    clean_iqr = cleaner.remove_outliers_iqr()
    print(clean_iqr)
    
    print("\nData after Z-score normalization:")
    normalized = cleaner.normalize_zscore()
    print(normalized)
    
    return cleaner

