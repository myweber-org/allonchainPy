def remove_duplicates(df, subset=None):
    """
    Remove duplicate rows from a DataFrame.
    
    Args:
        df (pd.DataFrame): Input DataFrame
        subset (list, optional): Columns to consider for duplicates
    
    Returns:
        pd.DataFrame: DataFrame with duplicates removed
    """
    return df.drop_duplicates(subset=subset, keep='first')

def handle_missing_values(df, strategy='mean', columns=None):
    """
    Handle missing values in DataFrame columns.
    
    Args:
        df (pd.DataFrame): Input DataFrame
        strategy (str): 'mean', 'median', 'mode', or 'drop'
        columns (list): Specific columns to process
    
    Returns:
        pd.DataFrame: DataFrame with handled missing values
    """
    df_copy = df.copy()
    
    if columns is None:
        columns = df_copy.columns
    
    for col in columns:
        if df_copy[col].isnull().any():
            if strategy == 'mean':
                df_copy[col].fillna(df_copy[col].mean(), inplace=True)
            elif strategy == 'median':
                df_copy[col].fillna(df_copy[col].median(), inplace=True)
            elif strategy == 'mode':
                df_copy[col].fillna(df_copy[col].mode()[0], inplace=True)
            elif strategy == 'drop':
                df_copy = df_copy.dropna(subset=[col])
    
    return df_copy

def normalize_columns(df, columns=None):
    """
    Normalize specified columns to range [0, 1].
    
    Args:
        df (pd.DataFrame): Input DataFrame
        columns (list): Columns to normalize
    
    Returns:
        pd.DataFrame: DataFrame with normalized columns
    """
    df_copy = df.copy()
    
    if columns is None:
        columns = df_copy.select_dtypes(include=[np.number]).columns
    
    for col in columns:
        if col in df_copy.columns and df_copy[col].dtype in [np.float64, np.int64]:
            col_min = df_copy[col].min()
            col_max = df_copy[col].max()
            
            if col_max != col_min:
                df_copy[col] = (df_copy[col] - col_min) / (col_max - col_min)
    
    return df_copy

def clean_dataframe(df, operations=None):
    """
    Apply multiple cleaning operations to DataFrame.
    
    Args:
        df (pd.DataFrame): Input DataFrame
        operations (dict): Dictionary of operations to apply
    
    Returns:
        pd.DataFrame: Cleaned DataFrame
    """
    if operations is None:
        operations = {
            'remove_duplicates': True,
            'handle_missing': {'strategy': 'mean'},
            'normalize': True
        }
    
    cleaned_df = df.copy()
    
    if operations.get('remove_duplicates', False):
        cleaned_df = remove_duplicates(cleaned_df)
    
    if 'handle_missing' in operations:
        handle_config = operations['handle_missing']
        strategy = handle_config.get('strategy', 'mean')
        columns = handle_config.get('columns', None)
        cleaned_df = handle_missing_values(cleaned_df, strategy, columns)
    
    if operations.get('normalize', False):
        columns = operations.get('normalize_columns', None)
        cleaned_df = normalize_columns(cleaned_df, columns)
    
    return cleaned_df