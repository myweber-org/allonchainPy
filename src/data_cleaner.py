import pandas as pd

def remove_outliers_iqr(df, columns):
    """
    Remove outliers using the Interquartile Range method.
    """
    cleaned_df = df.copy()
    for col in columns:
        Q1 = cleaned_df[col].quantile(0.25)
        Q3 = cleaned_df[col].quantile(0.75)
        IQR = Q3 - Q1
        lower_bound = Q1 - 1.5 * IQR
        upper_bound = Q3 + 1.5 * IQR
        cleaned_df = cleaned_df[(cleaned_df[col] >= lower_bound) & (cleaned_df[col] <= upper_bound)]
    return cleaned_df

def normalize_minmax(df, columns):
    """
    Normalize specified columns using Min-Max scaling.
    """
    normalized_df = df.copy()
    for col in columns:
        min_val = normalized_df[col].min()
        max_val = normalized_df[col].max()
        normalized_df[col] = (normalized_df[col] - min_val) / (max_val - min_val)
    return normalized_df

def handle_missing_values(df, strategy='mean'):
    """
    Handle missing values by imputation.
    """
    df_filled = df.copy()
    numeric_cols = df_filled.select_dtypes(include=[np.number]).columns
    
    if strategy == 'mean':
        for col in numeric_cols:
            df_filled[col].fillna(df_filled[col].mean(), inplace=True)
    elif strategy == 'median':
        for col in numeric_cols:
            df_filled[col].fillna(df_filled[col].median(), inplace=True)
    elif strategy == 'mode':
        for col in numeric_cols:
            df_filled[col].fillna(df_filled[col].mode()[0], inplace=True)
    else:
        raise ValueError("Strategy must be 'mean', 'median', or 'mode'")
    
    return df_filled

def clean_dataset(df, numeric_columns, outlier_removal=True, normalization=True, missing_strategy='mean'):
    """
    Main function to clean dataset with optional outlier removal and normalization.
    """
    df_cleaned = df.copy()
    
    if missing_strategy:
        df_cleaned = handle_missing_values(df_cleaned, strategy=missing_strategy)
    
    if outlier_removal:
        df_cleaned = remove_outliers_iqr(df_cleaned, numeric_columns)
    
    if normalization:
        df_cleaned = normalize_minmax(df_cleaned, numeric_columns)
    
    return df_cleaned