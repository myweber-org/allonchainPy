def calculate_summary_stats(df, column):
    """
    Calculate summary statistics for a column after outlier removal.
    
    Args:
        df (pd.DataFrame): Input DataFrame
        column (str): Column name to analyze
    
    Returns:
        dict: Dictionary containing summary statistics
    """
    stats = {
        'original_count': len(df),
        'cleaned_count': len(df),
        'mean': df[column].mean(),
        'median': df[column].median(),
        'std': df[column].std(),
        'min': df[column].min(),
        'max': df[column].max()
    }
    
    if 'cleaned_count' in stats:
        stats['outliers_removed'] = stats['original_count'] - stats['cleaned_count']
    
    return stats

def process_dataset(file_path, column_to_clean):
    """
    Main function to load and clean a dataset.
    
    Args:
        file_path (str): Path to CSV file
        column_to_clean (str): Column name to clean
    
    Returns:
        tuple: (cleaned_df, statistics)
    """
    try:
        df = pd.read_csv(file_path)
        cleaned_df = remove_outliers_iqr(df, column_to_clean)
        stats = calculate_summary_stats(cleaned_df, column_to_clean)
        
        return cleaned_df, stats
    except FileNotFoundError:
        print(f"Error: File '{file_path}' not found")
        return None, None
    except Exception as e:
        print(f"Error processing dataset: {str(e)}")
        return None, None

