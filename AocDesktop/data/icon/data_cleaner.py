def remove_outliers_iqr(data, column, factor=1.5):
    """
    Remove outliers using the Interquartile Range method.
    
    Args:
        data: pandas DataFrame
        column: column name to process
        factor: IQR multiplier (default 1.5)
    
    Returns:
        DataFrame with outliers removed
    """
    if column not in data.columns:
        raise ValueError(f"Column '{column}' not found in DataFrame")
    
    q1 = data[column].quantile(0.25)
    q3 = data[column].quantile(0.75)
    iqr = q3 - q1
    lower_bound = q1 - factor * iqr
    upper_bound = q3 + factor * iqr
    
    filtered_data = data[(data[column] >= lower_bound) & (data[column] <= upper_bound)]
    return filtered_data.copy()

def normalize_minmax(data, column):
    """
    Normalize data using Min-Max scaling to range [0, 1].
    
    Args:
        data: pandas DataFrame
        column: column name to normalize
    
    Returns:
        DataFrame with normalized column
    """
    if column not in data.columns:
        raise ValueError(f"Column '{column}' not found in DataFrame")
    
    min_val = data[column].min()
    max_val = data[column].max()
    
    if max_val == min_val:
        normalized = 0.5
    else:
        normalized = (data[column] - min_val) / (max_val - min_val)
    
    result = data.copy()
    result[f"{column}_normalized"] = normalized
    return result

def standardize_zscore(data, column):
    """
    Standardize data using Z-score normalization.
    
    Args:
        data: pandas DataFrame
        column: column name to standardize
    
    Returns:
        DataFrame with standardized column
    """
    if column not in data.columns:
        raise ValueError(f"Column '{column}' not found in DataFrame")
    
    mean_val = data[column].mean()
    std_val = data[column].std()
    
    if std_val == 0:
        standardized = 0
    else:
        standardized = (data[column] - mean_val) / std_val
    
    result = data.copy()
    result[f"{column}_standardized"] = standardized
    return result

def clean_missing_values(data, strategy='mean'):
    """
    Handle missing values in numeric columns.
    
    Args:
        data: pandas DataFrame
        strategy: 'mean', 'median', 'mode', or 'drop'
    
    Returns:
        DataFrame with missing values handled
    """
    numeric_cols = data.select_dtypes(include=[np.number]).columns
    
    if strategy == 'drop':
        cleaned = data.dropna(subset=numeric_cols)
    elif strategy == 'mean':
        cleaned = data.copy()
        for col in numeric_cols:
            cleaned[col].fillna(cleaned[col].mean(), inplace=True)
    elif strategy == 'median':
        cleaned = data.copy()
        for col in numeric_cols:
            cleaned[col].fillna(cleaned[col].median(), inplace=True)
    elif strategy == 'mode':
        cleaned = data.copy()
        for col in numeric_cols:
            cleaned[col].fillna(cleaned[col].mode()[0], inplace=True)
    else:
        raise ValueError("Strategy must be 'mean', 'median', 'mode', or 'drop'")
    
    return cleaned

def process_dataset(data, numeric_columns, outlier_factor=1.5, normalize=True, standardize=True):
    """
    Complete data processing pipeline.
    
    Args:
        data: pandas DataFrame
        numeric_columns: list of numeric column names to process
        outlier_factor: IQR factor for outlier removal
        normalize: whether to apply min-max normalization
        standardize: whether to apply z-score standardization
    
    Returns:
        Processed DataFrame
    """
    processed_data = data.copy()
    
    for column in numeric_columns:
        if column in processed_data.columns:
            processed_data = remove_outliers_iqr(processed_data, column, outlier_factor)
    
    processed_data = clean_missing_values(processed_data, strategy='mean')
    
    for column in numeric_columns:
        if column in processed_data.columns:
            if normalize:
                processed_data = normalize_minmax(processed_data, column)
            if standardize:
                processed_data = standardize_zscore(processed_data, column)
    
    return processed_data