    numeric_cols = df.select_dtypes(include=[np.number]).columns
    for col in numeric_cols:
        if np.isinf(df[col]).any():
            print(f"Warning: Column '{col}' contains infinite values")
    
    print("DataFrame validation passed")
    return True

