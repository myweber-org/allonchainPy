def clean_dataset(df, strategy='mean', outlier_threshold=3):
    """
    Clean a pandas DataFrame by handling missing values and removing outliers.
    
    Parameters:
    df (pd.DataFrame): Input DataFrame to clean.
    strategy (str): Strategy for filling missing values ('mean', 'median', 'mode', 'drop').
    outlier_threshold (float): Number of standard deviations for outlier detection.
    
    Returns:
    pd.DataFrame: Cleaned DataFrame.
    """
    cleaned_df = df.copy()
    
    # Handle missing values
    if strategy == 'drop':
        cleaned_df = cleaned_df.dropna()
    elif strategy in ['mean', 'median']:
        for column in cleaned_df.select_dtypes(include=[np.number]).columns:
            if strategy == 'mean':
                fill_value = cleaned_df[column].mean()
            else:
                fill_value = cleaned_df[column].median()
            cleaned_df[column].fillna(fill_value, inplace=True)
    elif strategy == 'mode':
        for column in cleaned_df.columns:
            fill_value = cleaned_df[column].mode()[0] if not cleaned_df[column].mode().empty else None
            if fill_value is not None:
                cleaned_df[column].fillna(fill_value, inplace=True)
    
    # Remove outliers for numerical columns
    numerical_cols = cleaned_df.select_dtypes(include=[np.number]).columns
    for column in numerical_cols:
        mean = cleaned_df[column].mean()
        std = cleaned_df[column].std()
        lower_bound = mean - outlier_threshold * std
        upper_bound = mean + outlier_threshold * std
        cleaned_df = cleaned_df[(cleaned_df[column] >= lower_bound) & (cleaned_df[column] <= upper_bound)]
    
    # Reset index after cleaning
    cleaned_df.reset_index(drop=True, inplace=True)
    
    return cleaned_df

def validate_data(df, required_columns=None, min_rows=1):
    """
    Validate DataFrame structure and content.
    
    Parameters:
    df (pd.DataFrame): DataFrame to validate.
    required_columns (list): List of required column names.
    min_rows (int): Minimum number of rows required.
    
    Returns:
    tuple: (is_valid, error_message)
    """
    if df.empty:
        return False, "DataFrame is empty"
    
    if len(df) < min_rows:
        return False, f"DataFrame has fewer than {min_rows} rows"
    
    if required_columns:
        missing_columns = [col for col in required_columns if col not in df.columns]
        if missing_columns:
            return False, f"Missing required columns: {missing_columns}"
    
    return True, "Data validation passed"

